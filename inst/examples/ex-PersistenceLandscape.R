
# double matrix
pl <- new(
  PersistenceLandscape,
  cbind(c(0, 0, 1, 2), c(1.5, 2.5, 3.5, 4.5)),
  TRUE,
  0, 10, .01
)
pl$isExact()
pl$xMin()
pl$xMax()
pl$xBy()
pl$getInternal()
print(pl)
summary(pl)

dim(pl$discretize()$getInternal())
length(as.vector(pl))
as.data.frame(pl)

pl2 <- pl$scale(2)
pl2$getInternal()
pl3 <- pl$add(pl2)
pl3$getInternal()

